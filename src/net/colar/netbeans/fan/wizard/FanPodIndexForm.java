    public void setValues(final SortedSet<String> values) {
        final SwingWorker<Integer,Integer> sw = new SwingWorker<Integer,Integer>() {
            @Override
            public Integer doInBackground() {
                final DefaultListModel model = (DefaultListModel) indexList.getModel();
                model.clear();
                int i = 0;
                for (String str : values) {
                    model.add(i, str);
                    i++;
                }
                return 0;
            }
        };
        sw.execute();
    }
    public SortedSet<String> getValues() {
        final SortedSet<String> set = new TreeSet<String>();
        final SwingWorker<Integer,Integer> sw = new SwingWorker<Integer,Integer>() {
            @Override
            public Integer doInBackground() {
                final DefaultListModel model = (DefaultListModel) indexList.getModel();
                for (int i = 0; i < model.getSize(); i++) {
                    set.add(model.get(i).toString());
                }
                return 0;
            }
        };
        sw.execute();
        // wait for return code.
        try {
            sw.get();
        } catch (InterruptedException ex) {
            Exceptions.printStackTrace(ex);
        } catch (ExecutionException ex) {
            Exceptions.printStackTrace(ex);
        }
        return set;
    }

