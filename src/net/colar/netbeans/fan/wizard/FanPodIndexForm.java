    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        final SwingWorker<Integer,Integer> sw = new SwingWorker<Integer,Integer>() {
            @Override
            public Integer doInBackground() {
                final String key = keyTextField.getText();
                final String value = valueTextField.getText();
                if (null == key || "".equals(key) ||
                    null == value || "".equals(value)) {
                    return 0;
                }
                final DefaultListModel model = (DefaultListModel) indexList.getModel();
                model.addElement(key + " : " + value);
                return 0;
            }
        };
        sw.execute();
    }//GEN-LAST:event_addButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        final SwingWorker<Integer,Integer> sw = new SwingWorker<Integer,Integer>() {
            @Override
            public Integer doInBackground() {
                final DefaultListModel model = (DefaultListModel) indexList.getModel();
                int[] indices = indexList.getSelectedIndices();
                for (int i = indices.length-1; i >= 0; i--) {
                    int row = indices[i];
                    model.remove(row);
                }
                return 0;
            }
        };
        sw.execute();
    }//GEN-LAST:event_deleteButtonActionPerformed

